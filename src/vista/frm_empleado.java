    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
        empleado obj_empleado = new empleado();
        obj_empleado.setCodigo_empleado(this.txt_codemp.getText());
        obj_empleado.setPuesto(this.txt_pues.getText());
        obj_empleado.setNombre(this.txt_nom.getText());
        obj_empleado.setApellido(this.txt_ape.getText());
        obj_empleado.setDireccion(this.txt_dir.getText());
        obj_empleado.setTelefono(this.txt_tel.getText());
        obj_empleado.setFecha_nacimiento(this.txt_fech.getText());
        
        obj_empleado.agregar();
    }//GEN-LAST:event_btn_agregarActionPerformed

